    private void loadReceptions() {
        try {
            List<ReceptionDTO> allReceptions = ReceptionController.getAllReceptions();
            cmbName.removeAllItems();
            if (allReceptions == null) {
                return;
            }

            for (ReceptionDTO receptionDTO : allReceptions) {
                cmbName.addItem(receptionDTO.getrName());
            }
        } catch (Exception ex) {
            Logger.getLogger(ReceptionHome.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
