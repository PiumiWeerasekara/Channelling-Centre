    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        try {
            ReceptionDTO reception = ReceptionController.receptionLogin(txtName.getText(), txtPassword.getText());
            if (reception != null) {

                boolean reserve = ReceptionController.reserveReception(txtName.getText());
                if (reserve) {
                    ReceptionHome r = new ReceptionHome(txtName.getText());
                    this.setVisible(false);
                    r.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Allredy login");
                }

            } else {
                JOptionPane.showMessageDialog(this, "You are not a Receptionist");
            }

            // TODO add your handling code here:
        } catch (Exception ex) {
            Logger.getLogger(ReceptionLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

