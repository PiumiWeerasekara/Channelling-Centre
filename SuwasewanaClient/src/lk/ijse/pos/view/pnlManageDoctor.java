    private void cmbDNameItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbDNameItemStateChanged
        try {
            String doctor=(String) cmbDName.getSelectedItem();
            DoctorDTO doctorDTO=DoctorController.findByName(doctor);
            txtDoctorID.setText(Integer.toString(doctorDTO.getdID()));
            txtSpecialityID.setText(Integer.toString(doctorDTO.getSpecialityID()));
            txtHospitalID.setText(Integer.toString(doctorDTO.gethID()));
            txtGID.setText(doctorDTO.getdRNo());
            txtAddress.setText(doctorDTO.getdAddress());
            txtNIC.setText(doctorDTO.getdNIC());
            txtContact.setText(doctorDTO.getdContact());
            txtHospital.setText(doctorDTO.getHospital());
            txtSpeciality.setText(doctorDTO.getSpetiality());
        } catch (Exception ex) {
            Logger.getLogger(pnlManageDoctor.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_cmbDNameItemStateChanged

