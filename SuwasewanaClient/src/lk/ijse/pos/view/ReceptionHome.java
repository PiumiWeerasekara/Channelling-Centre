    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        try {
            ReceptionDTO receptionDTO = new ReceptionDTO(Integer.parseInt(txtReceptionID.getText()),
                    (String) cmbName.getSelectedItem(),
                    txtAddress.getText(),
                    txtContact.getText(),
                    txtPassword.getText());
            boolean result = ReceptionController.updateReception(receptionDTO);
            if (result) {
                JOptionPane.showMessageDialog(this, "Receptionist has been updated successfully");
            } else {
                JOptionPane.showMessageDialog(this, "Receptionist hasn't been updated successfully");
            }

            // TODO add your handling code here:
        } catch (Exception ex) {
            Logger.getLogger(ReceptionHome.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnModifyActionPerformed

