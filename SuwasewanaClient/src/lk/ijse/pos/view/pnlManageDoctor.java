    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        try {
            DoctorDTO doctorDTO=new DoctorDTO(Integer.parseInt(txtDoctorID.getText()),
                    (String)cmbDName.getSelectedItem(),
                    Integer.parseInt(txtSpecialityID.getText()),
                    Integer.parseInt(txtHospitalID.getText()),
                    txtGID.getText(),
                    txtAddress.getText(),
                    txtNIC.getText(),
                    txtContact.getText(),
                    txtHospital.getText(),
                    txtSpeciality.getText());
            boolean result=DoctorController.updateDoctor(doctorDTO);
            if (result) {
                JOptionPane.showMessageDialog(this, "Doctor has been Updated successfullly");
                clearTextFields();
            }else{
                JOptionPane.showMessageDialog(this, "Doctor hasn't been Updated sucessfully ");
                clearTextFields();
            }
            // TODO add your handling code here:
        } catch (Exception ex) {
            Logger.getLogger(pnlManageDoctor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnModifyActionPerformed

