    private void loadDoctorNames() {
        try {
            List<DoctorDTO> doctors = DoctorController.getAllDoctors();
            cmbDName.removeAllItems();
            if (doctors == null) {
                return;
            }

            for (DoctorDTO doctor : doctors) {
                cmbDName.addItem(doctor.getdName());
                System.out.println("12321214312412412");
            }
        } catch (Exception ex) {
            Logger.getLogger(pnlManageDoctor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
